import '../../domain/entities/invoice_entity.dart';

/// Invoice data model with JSON serialization
class InvoiceModel extends InvoiceEntity {
  const InvoiceModel({
    String? id,
    required String invoiceNumber,
    required DateTime invoiceDate,
    required String billNo,
    String? referenceNo,
    required String companyName,
    required String companyAddress,
    required String companyPhone,
    required String companyEmail,
    required String companyGst,
    required String companyPan,
    required String customerVendorName,
    required String customerVendorAddress,
    required String customerVendorGst,
    required String transactionType,
    required String productName,
    required String productDescription,
    required double quantity,
    required double unitPrice,
    required double subtotal,
    required double gstRate,
    required double gstAmount,
    required double transportCharges,
    required double grandTotal,
    required String paymentMethod,
    required String paymentStatus,
    String? terms,
    String? notes,
    required DateTime createdAt,
    required DateTime updatedAt,
  }) : super(
    id: id,
    invoiceNumber: invoiceNumber,
    invoiceDate: invoiceDate,
    billNo: billNo,
    referenceNo: referenceNo,
    companyName: companyName,
    companyAddress: companyAddress,
    companyPhone: companyPhone,
    companyEmail: companyEmail,
    companyGst: companyGst,
    companyPan: companyPan,
    customerVendorName: customerVendorName,
    customerVendorAddress: customerVendorAddress,
    customerVendorGst: customerVendorGst,
    transactionType: transactionType,
    productName: productName,
    productDescription: productDescription,
    quantity: quantity,
    unitPrice: unitPrice,
    subtotal: subtotal,
    gstRate: gstRate,
    gstAmount: gstAmount,
    transportCharges: transportCharges,
    grandTotal: grandTotal,
    paymentMethod: paymentMethod,
    paymentStatus: paymentStatus,
    terms: terms,
    notes: notes,
    createdAt: createdAt,
    updatedAt: updatedAt,
  );

  factory InvoiceModel.fromJson(Map<String, dynamic> json) {
    return InvoiceModel(
      id: json['id'],
      invoiceNumber: json['invoiceNumber'],
      invoiceDate: DateTime.parse(json['invoiceDate']),
      billNo: json['billNo'],
      referenceNo: json['referenceNo'],
      companyName: json['companyName'],
      companyAddress: json['companyAddress'],
      companyPhone: json['companyPhone'],
      companyEmail: json['companyEmail'],
      companyGst: json['companyGst'],
      companyPan: json['companyPan'],
      customerVendorName: json['customerVendorName'],
      customerVendorAddress: json['customerVendorAddress'],
      customerVendorGst: json['customerVendorGst'],
      transactionType: json['transactionType'],
      productName: json['productName'],
      productDescription: json['productDescription'],
      quantity: (json['quantity'] as num).toDouble(),
      unitPrice: (json['unitPrice'] as num).toDouble(),
      subtotal: (json['subtotal'] as num).toDouble(),
      gstRate: (json['gstRate'] as num).toDouble(),
      gstAmount: (json['gstAmount'] as num).toDouble(),
      transportCharges: (json['transportCharges'] as num).toDouble(),
      grandTotal: (json['grandTotal'] as num).toDouble(),
      paymentMethod: json['paymentMethod'],
      paymentStatus: json['paymentStatus'],
      terms: json['terms'],
      notes: json['notes'],
      createdAt: DateTime.parse(json['createdAt']),
      updatedAt: DateTime.parse(json['updatedAt']),
    );
  }

  factory InvoiceModel.fromEntity(InvoiceEntity entity) {
    return InvoiceModel(
      id: entity.id,
      invoiceNumber: entity.invoiceNumber,
      invoiceDate: entity.invoiceDate,
      billNo: entity.billNo,
      referenceNo: entity.referenceNo,
      companyName: entity.companyName,
      companyAddress: entity.companyAddress,
      companyPhone: entity.companyPhone,
      companyEmail: entity.companyEmail,
      companyGst: entity.companyGst,
      companyPan: entity.companyPan,
      customerVendorName: entity.customerVendorName,
      customerVendorAddress: entity.customerVendorAddress,
      customerVendorGst: entity.customerVendorGst,
      transactionType: entity.transactionType,
      productName: entity.productName,
      productDescription: entity.productDescription,
      quantity: entity.quantity,
      unitPrice: entity.unitPrice,
      subtotal: entity.subtotal,
      gstRate: entity.gstRate,
      gstAmount: entity.gstAmount,
      transportCharges: entity.transportCharges,
      grandTotal: entity.grandTotal,
      paymentMethod: entity.paymentMethod,
      paymentStatus: entity.paymentStatus,
      terms: entity.terms,
      notes: entity.notes,
      createdAt: entity.createdAt,
      updatedAt: entity.updatedAt,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'invoiceNumber': invoiceNumber,
      'invoiceDate': invoiceDate.toIso8601String(),
      'billNo': billNo,
      'referenceNo': referenceNo,
      'companyName': companyName,
      'companyAddress': companyAddress,
      'companyPhone': companyPhone,
      'companyEmail': companyEmail,
      'companyGst': companyGst,
      'companyPan': companyPan,
      'customerVendorName': customerVendorName,
      'customerVendorAddress': customerVendorAddress,
      'customerVendorGst': customerVendorGst,
      'transactionType': transactionType,
      'productName': productName,
      'productDescription': productDescription,
      'quantity': quantity,
      'unitPrice': unitPrice,
      'subtotal': subtotal,
      'gstRate': gstRate,
      'gstAmount': gstAmount,
      'transportCharges': transportCharges,
      'grandTotal': grandTotal,
      'paymentMethod': paymentMethod,
      'paymentStatus': paymentStatus,
      'terms': terms,
      'notes': notes,
      'createdAt': createdAt.toIso8601String(),
      'updatedAt': updatedAt.toIso8601String(),
    };
  }
}
